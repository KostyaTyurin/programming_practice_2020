import turtle as tr
tr.shape('turtle')
tr.speed(10)
tr.color('black')
tr.begin_fill()
tr.color('black', 'yellow')
tr.circle(100)
tr.end_fill()


tr.hideturtle()
tr.penup()
tr.goto(-35,110)
tr.showturtle()
tr.pendown()
tr.begin_fill()
tr.color('black', 'red')
tr.circle(20)
tr.end_fill()

tr.penup()
tr.goto(35,110)
tr.showturtle()
tr.pendown()
tr.begin_fill()
tr.color('black', 'red')
tr.circle(20)
tr.end_fill()

tr.penup()
tr.goto(0, 100)
tr.showturtle()
tr.pendown()
tr.pencolor('black')
tr.width(10)
tr.right(90)
tr.fd(40)

tr.hideturtle()
tr.penup()
tr.goto(-55, 70)
tr.showturtle()
tr.pendown()
tr.pencolor('black')
tr.width(6)
tr.circle(55, 180)

tr.exitonclick()